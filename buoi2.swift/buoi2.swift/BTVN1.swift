func nhapDiem(x:Int){
    if x>=8 && x<=10{
        print("gioi")
    }else if x<=5 && x>=0{
        print("kha")
    }else if x>10 && x<0 {
        print("nhap lai")
    }else{
        print("trung binh")
    }
}
